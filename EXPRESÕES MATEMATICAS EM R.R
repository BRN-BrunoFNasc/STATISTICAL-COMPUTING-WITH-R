# CALCULADORA

# VARIAVEIS
a = 2
b = 3
n = 4
m = 5

#SOMA
print(a + b)
# SUBTRAÇÃO
print(a - b)
# MULTIPLICAÇÃO
print(a * b)
# DIVISÃO
print(a / b)
# POTENCIA
print(a ** b)
  # ou
print(a ^ b)
# RAIZ QUADRADA
print(sqrt(b))
# RAIZ ENÉSIMA
print(a ^ (n/m))
#MÓDULO
print(abs(a))
# RESTO DE DIVISÃO
print(a %% b)
# QUOCIENTE DA DIVISÃO
print(a %/% b)
# FATORIAL
print(factorial(m))
# LOGARITMO
print(log(a, base=b))
  # ou
print(log(a,b))